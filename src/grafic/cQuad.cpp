#include <iostream>

#include "cQuad.h"

/// ***********************************************************************
/// ** 
/// ***********************************************************************
cQuad::cQuad() {

	pMin	= cPoint();
	pMax 	= cPoint();
	qColor 	= cColor();

}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
cQuad::cQuad(cPoint p0, cPoint p1) {

	pMin.setCoords(p0);
	pMax.setCoords(p1);

	qColor = cColor();
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
cQuad::cQuad(cPoint p0, cPoint p1, cColor c) {

	pMin.setCoords(p0);
	pMax.setCoords(p1);
	
	qColor.setColor(c);
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
void cQuad::setMinPoint(cPoint p) {

		pMin.setCoords(p);
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
cPoint cQuad::getMinPoint() {

	return pMin;
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
void cQuad::setMaxPoint(cPoint p) {

		pMax.setCoords(p);
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
cPoint cQuad::getMaxPoint() {

	return pMax;
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
void cQuad::setQuadColor(cColor c) {

		qColor.setColor(c);
}


/// ***********************************************************************
/// ** 
/// ***********************************************************************
cColor cQuad::getQuadColor() {

	return qColor;
}

/// ***********************************************************************
/// ** 
/// ***********************************************************************
std::ostream &operator<<( std::ostream &output, const cQuad &q ) {

	output << "Quad : " << std::endl;
	output << "		pMin = " << q.pMin << std::endl;
	output << "		pMax = " << q.pMax << std::endl;
	output << "		color = " << q.qColor << std::endl;

	return output;            
}

